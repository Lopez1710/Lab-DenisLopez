    public void Cargar (){
    String titulos[]={"ID","NOMBRE","EDAD","EMAIL","TELEFONO"};
        
        DefaultTableModel df = new DefaultTableModel(null,titulos);
        
        Personas ps = new Personas();
        
        ArrayList <Persona> listar = ps.ListadoPersonas();
        
        Iterator iterador = listar.iterator();
        
        String fila[]= new String[5];
        while (iterador.hasNext()) { 
            Persona Prs = (Persona)iterador.next();
            fila[0]=String.valueOf(Prs.getId());
            fila[1]=Prs.getNombre();
            fila[2]= String.valueOf(Prs.getEdad());
            fila[3]=Prs.getEmail();
            fila[4]=String.valueOf(Prs.getNumeroDeTelefono());
            df.addRow(fila);
        }
        tblContacto.setModel(df);
    }
