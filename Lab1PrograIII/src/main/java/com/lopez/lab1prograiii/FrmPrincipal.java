    public void Agregar(){
        Personas prs= new Personas();
        Persona pr= new Persona();
        
        pr.setNombre(txtNombre.getText());
        pr.setEdad(Integer.valueOf(txtEdad.getText()));
        pr.setEmail(txtEmail.getText());
        pr.setNumeroDeTelefono(Integer.valueOf(txtTelefono.getText()));
        prs.Agregar(pr);
        Clean();
        Cargar();
    }
    
    public void Actualizar(){
        Personas prs= new Personas();
        Persona pr= new Persona();
        
       
            
            pr.setId(Integer.valueOf(txtId.getText()));
            pr.setNombre(txtNombre.getText());
            pr.setEdad(Integer.valueOf(txtEdad.getText()));
            pr.setEmail(txtEmail.getText());
            pr.setNumeroDeTelefono(Integer.valueOf(txtTelefono.getText()));
            prs.Actualizar(pr);
            Clean();
            Cargar();

    
    }
    public void Eliminar(){
        Personas prs= new Personas();
        Persona pr= new Persona();
        if (txtId.getText().equals("")) {
            JOptionPane.showMessageDialog(null,"No se puede eliminar");
        }else{
            
            pr.setId(Integer.valueOf(txtId.getText()));
        
            prs.Eliminar(pr);
            Clean();
            Cargar();
        }
        
    }
